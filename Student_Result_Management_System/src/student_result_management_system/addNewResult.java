    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
       String pin_no = (String)jTextField1.getText();
       String course = (String)jComboBox2.getSelectedItem();
       String branch = (String)jComboBox3.getSelectedItem();
       String year = (String)jComboBox1.getSelectedItem();
       String sub1=(String) jLabel7.getText();
       String sub2 =(String) jLabel8.getText();
       String sub3 =(String) jLabel9.getText();
       String sub4 =(String) jLabel10.getText();
       String sub1m = (String)jTextField6.getText();
       String sub2m = (String)jTextField7.getText();
       String sub3m = (String)jTextField8.getText();
       String sub4m = (String)jTextField9.getText();
       String result ="";
       if(course.equals("B TECH") && year.equals("1")){
           result = "btech1";
       }else if(course.equals("B TECH") && year.equals("2")){
           result = "btech2";
       }else if(course.equals("B TECH") && year.equals("3")){
           result = "btech3";
       }else if(course.equals("M TECH") && year.equals("1")){
           result = "mtech1";
       }else if(course.equals("M TECH") && year.equals("2")){
           result = "mtech2";
       }else if(course.equals("MBA") && year.equals("1")){
           result = "mba1";
       }else if(course.equals("MBA") && year.equals("2")){
           result = "mba2";
       }
       
       try{
           Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","Harinath@321");
            
            Statement st = con.createStatement();
            ResultSet rs =st.executeQuery("select * from student where pin_no='"+pin_no+"'");
            if(rs.next()){
                st.executeUpdate("insert into "+result+" (pin_no,"+sub1+","+sub2+","+sub3+","+sub4+",branch) values('"+pin_no+"','"+sub1m+"','"+sub2m+"','"+sub3m+"','"+sub4m+"','"+branch+"')");
                JOptionPane.showMessageDialog(null, "saved");
                setVisible(false);
                new addNewResult().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null,"student not registered!");
            }
       }catch (Exception e){
           JOptionPane.showMessageDialog(null,e.toString());
       }
    }//GEN-LAST:event_jButton7ActionPerformed

